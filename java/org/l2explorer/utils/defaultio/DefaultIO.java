package org.l2explorer.utils.defaultio;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation used to mark classes that support default serialization/deserialization.
 * <p>In the modernized L2Explorer core, this is used by the ReflectiveObjectIO
 * to identify classes that can be automatically processed via Reflection.</p>
 *
 * @author acmi (Original Code)
 * @author Galagard (L2Explorer Modernization)
 * @since 13-01-2026
 */
@Target(ElementType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
public @interface DefaultIO {
}